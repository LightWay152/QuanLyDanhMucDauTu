package dao.nbm.vn.model;

public class TienNapRut {
	private String soTK;
	private int sttNRT;
	private String ngayNRT;
	private String lenhNRT;
	private float soTienNapRut;
	private float soDuTienMatHT;
	private float tongTaiSanHT;
	
	public String getSoTK() {
		return soTK;
	}
	public void setSoTK(String soTK) {
		this.soTK = soTK;
	}
	public int getSttNRT() {
		return sttNRT;
	}
	public void setSttNRT(int sttNRT) {
		this.sttNRT = sttNRT;
	}
	public String getNgayNRT() {
		return ngayNRT;
	}
	public void setNgayNRT(String ngayNRT) {
		this.ngayNRT = ngayNRT;
	}
	public String getLenhNRT() {
		return lenhNRT;
	}
	public void setLenhNRT(String lenhNRT) {
		this.lenhNRT = lenhNRT;
	}
	public float getSoTienNapRut() {
		return soTienNapRut;
	}
	public void setSoTienNapRut(float soTienNapRut) {
		this.soTienNapRut = soTienNapRut;
	}
	public float getSoDuTienMatHT() {
		return soDuTienMatHT;
	}
	public void setSoDuTienMatHT(float soDuTienMatHT) {
		this.soDuTienMatHT = soDuTienMatHT;
	}
	public float getTongTaiSanHT() {
		return tongTaiSanHT;
	}
	public void setTongTaiSanHT(float tongTaiSanHT) {
		this.tongTaiSanHT = tongTaiSanHT;
	}

}
